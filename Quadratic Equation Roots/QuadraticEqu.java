
class QuadraticEqu {
    private double a;
    private double b;
    private double c;

    // Constructor
    public QuadraticEqu(double a, double b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }
	
	// Getter and setter for 'a'
    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }
	
	// Getter and setter for 'b'
    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }
	
	// Getter and setter for 'c'
    public double getC() {
        return c;
    }

    public void setC(double c) {
        this.c = c;
    }
}